def most_common_melody_pitch_and_tone(melody):

  mel_pitches = melody_pitches(melody)
  mel_tones = [t % 12 for t in mel_pitches]

  return [list(Counter(mel_pitches).most_common()[0]), list(Counter(mel_tones).most_common()[0])]

